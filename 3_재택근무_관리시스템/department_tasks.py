def get_department_tasks(department: str) -> list:
    """
    부서에 맞는 업무 템플릿 반환
    Args:
        department: 부서명
    Returns:
        해당 부서의 업무 템플릿 리스트
    """
    # 정확히 일치하는 부서가 있으면 반환
    if department in DEPARTMENT_TASKS:
        return DEPARTMENT_TASKS[department]
    
    # 부서명에 키워드가 포함되어 있으면 매칭
    for dept_key in DEPARTMENT_TASKS.keys():
        if dept_key in department or department in dept_key:
            return DEPARTMENT_TASKS[dept_key]
    
    # 매칭되는 부서가 없으면 기타 템플릿 반환
    return DEPARTMENT_TASKS["기타"]
